# ask user to input
input_number_one = int(input("Input First Number:"))
input_number_two = int(input("Input Second Number:"))

# check if inputted two numbers are the same
if input_number_one == input_number_two:
     # if same, print "Equal"
     print("Equal") 
else:
     # if not same, print "Not Equal" 
     print("Not Equal")